package com.meeting.pojo;

public class Seat {
    private Integer senum;

    private Long mnum;

    private Integer snum;

    private Integer pnum;

    public Integer getSenum() {
        return senum;
    }

    public void setSenum(Integer senum) {
        this.senum = senum;
    }

    public Long getMnum() {
        return mnum;
    }

    public void setMnum(Long mnum) {
        this.mnum = mnum;
    }

    public Integer getSnum() {
        return snum;
    }

    public void setSnum(Integer snum) {
        this.snum = snum;
    }

    public Integer getPnum() {
        return pnum;
    }

    public void setPnum(Integer pnum) {
        this.pnum = pnum;
    }
}