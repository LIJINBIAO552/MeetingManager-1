package com.meeting.pojo;

public class Hotel {
    private Integer hnum;

    private Long mnum;

    private Integer htnum;

    private Integer pnum;

    public Integer getHnum() {
        return hnum;
    }

    public void setHnum(Integer hnum) {
        this.hnum = hnum;
    }

    public Long getMnum() {
        return mnum;
    }

    public void setMnum(Long mnum) {
        this.mnum = mnum;
    }

    public Integer getHtnum() {
        return htnum;
    }

    public void setHtnum(Integer htnum) {
        this.htnum = htnum;
    }

    public Integer getPnum() {
        return pnum;
    }

    public void setPnum(Integer pnum) {
        this.pnum = pnum;
    }
}