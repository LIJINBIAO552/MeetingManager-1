package com.meeting.pojo;

/*
 * 扩展座位表（seat）
 * 实现多表查询，seat和participants
 * */
public class ExpandedSeat {
	
	int pnum;
	String pname;
	public int getPnum() {
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	
}
